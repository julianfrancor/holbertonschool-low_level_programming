#include <stdio.h>

/**
 * add - function that adds two integers
 * and returns the result.
 * @a: input intiger
 * @b: input intiger
 * Return: int
 */
int add(int a, int b)
{
	int result;

	result = a + b;
	return (result);
}

/**
 * sub - function that substract two integers
 * and returns the result.
 * @a: input intiger
 * @b: input intiger
 * Return: int
 */
int sub(int a, int b)
{
	int result;

	result = a - b;
	return (result);
}

/**
 * mul - function that multiply two integers
 * and returns the result.
 * @a: input intiger
 * @b: input intiger
 * Return: int
 */
int mul(int a, int b)
{
	int result;

	result = a * b;
	return (result);
}

/**
 * div - function that divide two integers
 * and returns the result.
 * @a: input intiger
 * @b: input intiger
 * Return: int
 */
int div(int a, int b)
{
	int result;

	result = a / b;
	return (result);
}

/**
 * mod - function that mod two integers
 * and returns the result.
 * @a: input intiger
 * @b: input intiger
 * Return: int
 */
int mod(int a, int b)
{
	int result;

	result = a % b;
	return (result);
}
